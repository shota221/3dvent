        この時、この機器の使用経験が無い場合は、初期値として設定圧20cmH₂O,空気流量9L/min, 酸素流量3L/minを入力します。（使用経験がある場合、患者の状態を想像しながら推測し初期値を設定します）
    </p>
    <p>
        4. 呼吸数が早すぎる場合は+2.5cmH₂O刻みに設定圧を高めます（＜40cmH₂O）。<br>
        遅すぎる場合は-2.5cmH₂O刻みに設定圧を低めます。(＞8cmH₂O)。<br>
        SpO₂が92％未満の場合は、酸素流量を増やし、空気流量を減らし、合計流量が12L/minとなるようにします。
    <p>
        5. アプリケーションにその設定圧、空気流量、酸素流量を入力します。
    </p>
    <p>
        6. FiO₂と推定PEEPが表示されます。
    </p>
    <p>
        7. 呼吸数正常とSpO₂＞92%の両者のチェックボックスをチェックします。
    </p>
    <p>
        8. 次に、Vt, VMを推定するために動作音収集測定（または手動測定）に進みます
    </p>
    <p>
        9. Vt, VMの推定値を確認し登録します。
    </p>
</div>
