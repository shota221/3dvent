    </p>
    <p>
        2. 推定MVが低く、呼吸数が少なすぎる場合は、合計流量を3L/min増やします。この時酸素流量と空気流量の比率を変えない様にします。
    </p>
    <p>
        3. SpO₂＞92％にするために、FiO₂が50％を超えているときは、空気流量のみを増加させ、合計流量を増加させることで、推定MVを上昇させると同時にFiO₂が低下します。呼吸数が高すぎる場合は、設定圧を増加させます。
    </p>
    <p>
        4. アプリケーションにその設定圧、空気流量、酸素流量を入力しなおします。
    </p>
    <p>
        5. FiO₂と推定PEEPが表示されます。
    </p>
    <p>
        6. 呼吸数正常とSpO₂＞92%の両者のチェックボックスをチェックします。
    </p>
    <p>
        7. 次に、Vt, VMを推定するために動作音収集測定（または手動測定）に進みます。
    </p>
    <p>
        8. Vt, VMの推定値を確認し登録します。
    </p>
